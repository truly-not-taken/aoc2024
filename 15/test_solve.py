def test_gps_1():
    data = """##########
#.O.O.OOO#
#........#
#OO......#
#OO@.....#
#O#.....O#
#O.....OO#
#O.....OO#
#OO....OO#
##########
"""

    warehouse = [list(line) for line in data.splitlines()]

    assert 10092 == solve.gps(warehouse)

