def test_solve_small():
    puzzle =  """########
#..O.O.#
##@.O..#
#...O..#
#.#.O..#
#...O..#
#......#
########

<^^>>>vv<v>>v<<
"""

    assert (2028, 1751) == solve.solve(puzzle)

